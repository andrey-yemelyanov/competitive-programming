	public static int countLeapDays(Date from, Date to){
		int count = 0;
		int fromYear = from.year, toYear = to.year;

		while(!isLeapYear(fromYear) && fromYear < toYear){
			fromYear++;
		}
		
		while(!isLeapYear(toYear) && toYear > fromYear){
			toYear--;
		}
		
		if(fromYear == toYear && !isLeapYear(fromYear)) return 0;
		
		int diff = toYear - fromYear;
		int totalLeapYears = diff / 4 + 1;
		if(fromYear == from.year && !beforeOrOnFeb29(from)) totalLeapYears--;
		if(toYear == to.year && !afterOrOnFeb29(to)) totalLeapYears--;
		
		totalLeapYears -= (toYear / 100) - (fromYear / 100);
		totalLeapYears += (toYear / 400) - (fromYear / 400);
		count += totalLeapYears;
		
		return count;
	}
	
	static boolean beforeOrOnFeb29(Date date){
		return date.month <= 2;
	}
	
	static boolean afterOrOnFeb29(Date date){
		return date.month > 2 || (date.month == 2 && date.day == 29);
	}
	
	public static boolean isLeapYear(int year){
		return (year % 4 == 0 && year % 100 != 0) || (year % 400 == 0);
	}
