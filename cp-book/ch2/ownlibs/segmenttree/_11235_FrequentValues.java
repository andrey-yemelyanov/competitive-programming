	public static class SegmentTree{
		public SegmentTree(int[] input){
			this.input = buildInput(input);
			st = new int[input.length * 4];
			buildStart(input);
			buildTree(1, 0, input.length - 1);
		}
		private int[] st;
		private int[] start;
		private int[] input;
		private void buildStart(int[] input){
			start = new int[input.length];
			int s = 0;
			start[0] = s;
			for(int i = 1; i < input.length; i++){
				if(input[i] != input[i - 1]){
					s = i;
				}
				start[i] = s;
			}
		}
		private int[] buildInput(int[] input){
			int[] array = new int[input.length];
			Map<Integer, Integer> m = new HashMap<Integer, Integer>();
			for(int i = 0; i < input.length; i++){
				if(!m.containsKey(input[i])){
					m.put(input[i], 0);
				}
				m.put(input[i], m.get(input[i]) + 1);
			}
			for(int i = 0; i < input.length; i++){
				array[i] = m.get(input[i]);
			}
			return array;
		}
		private int left(int p) {return p << 1;}
		private int right(int p) {return (p << 1) + 1;}
		private void buildTree(int p, int left, int right){
			if(left == right) st[p] = left;
			else{
				buildTree(left(p), left, (left + right) / 2);
				buildTree(right(p), (left + right) / 2 + 1, right);
				int p1 = st[left(p)], p2 = st[right(p)];
				st[p] = (input[p1] >= input[p2]) ? p1 : p2;
			}
		}
		private int rmq(int p, int left, int right, int i, int j){
			if(i > right || j < left) return -1;
			if(left >= i && right <= j) return st[p];
			int p1 = rmq(left(p), left, (left + right) / 2, i, j);
			int p2 = rmq(right(p), (left + right) / 2 + 1, right, i, j);
			if(p1 == -1) return p2;
			if(p2 == -1) return p1;
			return (input[p1] >= input[p2]) ? p1 : p2;
		}
		
		public int rfq(int i, int j){
			int leftFreq = input[i] - (i - start[i]);
			int rightFreq = j - start[j] + 1;
			int rmqFrom = start[i] + input[i];
			int rmqTo = start[j] - 1;
			int midFreq = 0;
			// find rmq on the mid-range
			if(rmqFrom <= rmqTo){
				midFreq = input[rmq(1, 0, input.length - 1, rmqFrom, rmqTo)];
			}
			// return max freq from the three sub-ranges
			return Math.max(leftFreq, Math.max(rightFreq, midFreq));
		}
	}
